		private List<Infrastructure.Data.Entities.Tables.BSD.ROH.OfferRequests.OfferRequestEmailAttachmentsEntity> GetAttachmentsIds(List<int> OffersIds)
		{
			try
			{
				return Infrastructure.Data.Access.Tables.BSD.ROH.OfferRequests.OfferRequestEmailAttachmentsAccess.GetAttachmentsPerOffers(OffersIds);

			} catch(Exception e)
			{
				Infrastructure.Services.Logging.Logger.Log(e);
			}
			return new List<Infrastructure.Data.Entities.Tables.BSD.ROH.OfferRequests.OfferRequestEmailAttachmentsEntity>();

		}
		private bool SendEmailToSuppliers(string Subject, string content, List<string> adresses, List<string> ccs, List<Infrastructure.Services.Files.Models.FileContent> attachments)
		{
			if(attachments == null || attachments.Count == 0)
			{
				return Module.EmailingService.SendEmailAsync(Subject, content, toAddresses: adresses, toAddressesCC: ccs).Result;
			}
			else
			{
				var attachmentList = new List<KeyValuePair<string, System.IO.Stream>>();

				foreach(var attachment in attachments)
				{
					attachmentList.Add(new KeyValuePair<string, System.IO.Stream>(
						attachment.FileName + attachment.FileExtension,
						new System.IO.MemoryStream(attachment.FileBytes)));
				}
				return Module.EmailingService.SendEmailAsync(Subject, content, toAddresses: adresses, attachments: attachmentList, toAddressesCC: ccs).Result;
			}
		}
		public string IsValidEmailAdress(string adresses)
		{

			if(!Infrastructure.Services.Email.Helpers.IsValidEmail(adresses))
			{
				return adresses;
			}

			return string.Empty;
		}
		private bool IsValidEmailContent(string content)
		{
			return !(string.IsNullOrEmpty(content) || string.IsNullOrWhiteSpace(content));
		}
		//
		private Infrastructure.Services.Files.Models.FileContent GetAttachment(int FileId)
		{
			try
			{
				return Program.FilesManager.GetFile(FileId);

			} catch(Exception e)
			{
				Infrastructure.Services.Logging.Logger.Log(e);
			}
			return null;
		}
