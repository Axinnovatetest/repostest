		#region Logs Generation
		private List<Infrastructure.Data.Entities.Tables.BSD.ROH.OfferRequests.__BSD_Offer_Request_LogsEntity> GenerateLogs(List<int> Ids,List<int> IdsToUpdateTheirStatus)
		{
			var logs = new List<Infrastructure.Data.Entities.Tables.BSD.ROH.OfferRequests.__BSD_Offer_Request_LogsEntity>();
			var offerEnities = Infrastructure.Data.Access.Tables.BSD.ROH.OfferRequests.ArticleOfferRequestsAccess.Get(Ids);

			foreach(var Id in offerEnities)
			{
				if(IdsToUpdateTheirStatus.Contains(Id.Id))
				{
					logs.Add(
										new Infrastructure.Data.Entities.Tables.BSD.ROH.OfferRequests.__BSD_Offer_Request_LogsEntity()
										{
											LastUpdateUserId = _user.Id,
											LastUpdateUsername = _user.Username,
											LastUpdateTime = DateTime.Now,
											LastUpdateUserFullName = _user.Name,
											LogDescription = $"The Offer request Email  has been Sent to  the supplier",
											LogObjectId = Id.Id,
											ManufacturerNumber = Id.ManufactuerNumber,
											SupplierContactName = Id.SupplierContactName,
											LogObject = "BSD Offer Request"
										}
										);
				}
				else
				{
					logs.Add(
										new Infrastructure.Data.Entities.Tables.BSD.ROH.OfferRequests.__BSD_Offer_Request_LogsEntity()
										{
											LastUpdateUserId = _user.Id,
											LastUpdateUsername = _user.Username,
											LastUpdateTime = DateTime.Now,
											LastUpdateUserFullName = _user.Name,
											LogDescription = $"The Offer request Email  Failed to be Delivered ",
											LogObjectId = Id.Id,
											ManufacturerNumber = Id.ManufactuerNumber,
											SupplierContactName = Id.SupplierContactName,
											LogObject = "BSD Offer Request"
										}
										);
				}
				
			}
			return logs;
		}
